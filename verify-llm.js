const SERVER = process.argv[2] || "http://localhost:3000";
const SAMPLE_SIZE = parseInt(process.argv[3] || "6", 10);

function pickRandom(arr, n) {
  const copy = [...arr];
  const out = [];
  for (let i = 0; i < n && copy.length > 0; i++) {
    const idx = Math.floor(Math.random() * copy.length);
    out.push(copy.splice(idx, 1)[0]);
  }
  return out;
}

async function ask(question) {
  const start = Date.now();
  const res = await fetch(`${SERVER}/ask`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ question }),
  });
  const data = await res.json();
  const elapsedMs = Date.now() - start;
  return { question, answer: data.answer, elapsedMs };
}

(async () => {
  console.log(`질문 풀 크기: ${QUESTIONS.length}개 (프리셋 6개와는 별개)`);
  console.log(`무작위로 ${SAMPLE_SIZE}개를 골라 ${SERVER}/ask 에 전송합니다.\n`);

  const sample = pickRandom(QUESTIONS, SAMPLE_SIZE);
  for (const q of sample) {
    const { answer, elapsedMs } = await ask(q);
    console.log(`Q: ${q}`);
    console.log(`A: ${answer}`);
    console.log(`(응답 시간: ${elapsedMs}ms)\n`);
  }
})();
